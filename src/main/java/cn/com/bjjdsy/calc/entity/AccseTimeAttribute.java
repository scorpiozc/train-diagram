package cn.com.bjjdsy.calc.entity;

public class AccseTimeAttribute {

	private String accseStationCode;
	private int dateType;
	private int beginTime;
	private int timeAttribute;

	public String getAccseStationCode() {
		return accseStationCode;
	}

	public void setAccseStationCode(String accseStationCode) {
		this.accseStationCode = accseStationCode;
	}

	public int getDateType() {
		return dateType;
	}

	public void setDateType(int dateType) {
		this.dateType = dateType;
	}

	public int getBeginTime() {
		return beginTime;
	}

	public void setBeginTime(int beginTime) {
		this.beginTime = beginTime;
	}

	public int getTimeAttribute() {
		return timeAttribute;
	}

	public void setTimeAttribute(int timeAttribute) {
		this.timeAttribute = timeAttribute;
	}

}
